/**
 * Configuration Baseline (v6.9.27)
 *
 * Safety-critical releases require a tamper-evident baseline: hash-pinned
 * source, toolchain version, compiler flags, library versions, qualified-
 * tool list per ISO 26262 Tool Confidence Level. This library writes a
 * baseline manifest into `.ctoc/baselines/<version>/manifest.yaml` whenever
 * `config_baseline` control is active.
 *
 * Captures:
 *   - VERSION
 *   - The Secure Hash Algorithm 256 of every source file in src/, agents/,
 *     skills/, .ctoc/regulatory-regimes/, plans/, docs/
 *   - The Node version
 *   - The package.json contents if present
 *   - Active regulatory profiles
 *   - Active tool-qualification records
 *
 * Cross-platform Node 18+, no native deps.
 *
 * References:
 *   - Plurilock — Configuration baseline:
 *     https://plurilock.com/glossary/configuration-baseline/
 *   - Visure — Safety-critical software process:
 *     https://visuresolutions.com/alm-guide/software-development-process-for-safety-critical-systems/
 */

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const BASELINES_ROOT = '.ctoc/baselines';

// Source directories to include in the baseline digest.
const SOURCE_DIRS = [
  'src',
  'agents',
  'skills',
  'docs',
  '.ctoc/regulatory-regimes',
  '.ctoc/tool-qualification',
  '.ctoc/templates',
];

/**
 * Compute the baseline for the current version and write it to disk.
 * Idempotent — calling twice for the same version produces the same digest.
 *
 * @param {string} projectRoot
 * @param {Object} opts - {version?: string, force?: boolean, activeRegimes?: string[]}
 * @returns {{version, manifest_path, baseline_hash}}
 */
function writeBaseline(projectRoot, opts = {}) {
  const version = opts.version || readVersion(projectRoot);
  if (!version) throw new Error('config-baseline.writeBaseline: VERSION file missing or empty');

  const baselineDir = path.join(projectRoot, BASELINES_ROOT, version);
  const manifestPath = path.join(baselineDir, 'manifest.yaml');
  if (fs.existsSync(manifestPath) && !opts.force) {
    // Existing baseline — read and return without rewriting.
    const content = fs.readFileSync(manifestPath, 'utf8');
    const m = content.match(/^baseline_hash:\s+(\S+)$/m);
    return {
      version,
      manifest_path: path.relative(projectRoot, manifestPath),
      baseline_hash: m ? m[1] : null,
      pre_existing: true,
    };
  }

  ensureDir(baselineDir);

  const fileEntries = [];
  for (const rel of SOURCE_DIRS) {
    const full = path.join(projectRoot, rel);
    if (!fs.existsSync(full)) continue;
    walkFiles(full, (filepath) => {
      const relpath = path.relative(projectRoot, filepath);
      const sha256 = hashFile(filepath);
      fileEntries.push({ relpath, sha256 });
    });
  }
  fileEntries.sort((a, b) => a.relpath.localeCompare(b.relpath));

  // Baseline hash = SHA-256 of `<sha256>  <relpath>\n` concatenation.
  const baselineHashInput = fileEntries.map(f => `${f.sha256}  ${f.relpath}`).join('\n');
  const baselineHash = crypto.createHash('sha256').update(baselineHashInput).digest('hex');

  // Capture toolchain.
  const nodeVersion = process.version;
  let packageJson = null;
  const pkgPath = path.join(projectRoot, 'package.json');
  if (fs.existsSync(pkgPath)) {
    try { packageJson = JSON.parse(fs.readFileSync(pkgPath, 'utf8')); } catch {}
  }
  const dependencies = packageJson && packageJson.dependencies ? packageJson.dependencies : {};
  const devDependencies = packageJson && packageJson.devDependencies ? packageJson.devDependencies : {};

  // Active regulatory profiles.
  const regimes = opts.activeRegimes || readActiveRegimes(projectRoot);

  // Tool-qualification records.
  const toolQualPath = path.join(projectRoot, '.ctoc/tool-qualification');
  let qualifiedTools = [];
  if (fs.existsSync(toolQualPath)) {
    qualifiedTools = fs.readdirSync(toolQualPath)
      .filter(f => f.endsWith('.yaml') && !f.startsWith('_'))
      .map(f => f.replace(/\.yaml$/, ''));
  }

  // Compose the manifest YAML.
  const now = new Date().toISOString();
  const lines = [
    `# Configuration Baseline for CTOC version ${version}`,
    `# Generated by src/lib/config-baseline.js`,
    `# Activated when the regulatory regime includes the config_baseline control.`,
    ``,
    `version: ${version}`,
    `baseline_hash: ${baselineHash}`,
    `created_at: ${now}`,
    `node_version: ${nodeVersion}`,
    `file_count: ${fileEntries.length}`,
    ``,
    `active_regulatory_regimes:`,
    ...(regimes.length === 0 ? ['  - (none)'] : regimes.map(r => `  - ${r}`)),
    ``,
    `qualified_tools:`,
    ...(qualifiedTools.length === 0 ? ['  - (none)'] : qualifiedTools.map(t => `  - ${t}`)),
    ``,
    `dependencies:`,
    ...Object.entries(dependencies).map(([k, v]) => `  ${k}: ${v}`),
    ``,
    `dev_dependencies:`,
    ...Object.entries(devDependencies).map(([k, v]) => `  ${k}: ${v}`),
    ``,
  ].join('\n');

  fs.writeFileSync(manifestPath, lines);

  // File hashes go into a separate file (large, line-oriented).
  fs.writeFileSync(
    path.join(baselineDir, 'file-hashes.txt'),
    fileEntries.map(f => `${f.sha256}  ${f.relpath}`).join('\n') + '\n'
  );

  return {
    version,
    manifest_path: path.relative(projectRoot, manifestPath),
    baseline_hash: baselineHash,
    file_count: fileEntries.length,
    pre_existing: false,
  };
}

/**
 * Verify that the current source tree still matches a previously written baseline.
 * Returns the list of files whose hash has changed since the baseline.
 */
function verifyBaseline(projectRoot, version) {
  const hashesPath = path.join(projectRoot, BASELINES_ROOT, version, 'file-hashes.txt');
  if (!fs.existsSync(hashesPath)) {
    throw new Error(`config-baseline.verifyBaseline: no baseline for version ${version}`);
  }
  const expected = {};
  for (const line of fs.readFileSync(hashesPath, 'utf8').split('\n')) {
    const m = line.match(/^(\S+)\s+(\S.+)$/);
    if (m) expected[m[2]] = m[1];
  }
  const drift = [];
  for (const [rel, expectedHash] of Object.entries(expected)) {
    const full = path.join(projectRoot, rel);
    if (!fs.existsSync(full)) {
      drift.push({ relpath: rel, status: 'missing', expected: expectedHash });
      continue;
    }
    const actual = hashFile(full);
    if (actual !== expectedHash) {
      drift.push({ relpath: rel, status: 'changed', expected: expectedHash, actual });
    }
  }
  // Also detect new files not in the baseline.
  const seen = new Set(Object.keys(expected));
  for (const rel of SOURCE_DIRS) {
    const full = path.join(projectRoot, rel);
    if (!fs.existsSync(full)) continue;
    walkFiles(full, (filepath) => {
      const relpath = path.relative(projectRoot, filepath);
      if (!seen.has(relpath)) {
        drift.push({ relpath, status: 'added', actual: hashFile(filepath) });
      }
    });
  }
  return { version, drift, drift_count: drift.length, ok: drift.length === 0 };
}

function readVersion(projectRoot) {
  const versionPath = path.join(projectRoot, 'VERSION');
  if (!fs.existsSync(versionPath)) return null;
  return fs.readFileSync(versionPath, 'utf8').trim();
}

function readActiveRegimes(projectRoot) {
  try {
    const { loadActiveProfiles } = require('./regulatory-regime');
    return loadActiveProfiles(projectRoot).profiles;
  } catch {
    return [];
  }
}

function walkFiles(dir, callback) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.name.startsWith('.git')) continue;
    if (entry.name === 'node_modules') continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walkFiles(full, callback);
    else if (entry.isFile()) callback(full);
  }
}

function hashFile(filepath) {
  const data = fs.readFileSync(filepath);
  return crypto.createHash('sha256').update(data).digest('hex');
}

function ensureDir(dir) {
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
}

module.exports = {
  writeBaseline,
  verifyBaseline,
  BASELINES_ROOT,
  SOURCE_DIRS,
};
